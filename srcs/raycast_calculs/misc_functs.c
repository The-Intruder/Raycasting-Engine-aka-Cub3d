#include "../cub3d.h"

/* -------------------------------------------------------------------------- */

float	deg2rad(int deg)
{
    return (deg * (M_PI / 180));
}

/* -------------------------------------------------------------------------- */

int	rad2deg(float rad)
{
    return (rad * (180 / M_PI));
}

/* -------------------------------------------------------------------------- */

float	normalize_angle(float rotation)
{
	rotation = fmodf(rotation, (M_PI * 2));
	if (rotation < 0)
		rotation += M_PI * 2;
	return (rotation);
}
